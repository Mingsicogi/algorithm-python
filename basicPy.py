# 사전 자료형(Map) - 사전 자료형은 키와 값의 쌍을 데이터로 가지는 자료형 O(n)
data = dict()
data['apple'] = '사과'
data['banana'] = '바나나'

print(data)

if 'apple' in data :
    print("'사과가 존재합니다'")

for key in data.keys():
    print(key)

for value in data.values():
    print(value)

data2 = {
    'apple' : '사과',
    'banana' : '바나나'
}
print(data2)

array = list(data2.values()) # list type casting
print(array)

# 집합 자료형(Set) - O(n)
