data4 = set([1,2,3,4,5,6,10,11,12])
print(data4)
print('--------------------------------------')
print(data3 & data4)
print(data3 | data4)
print(data3 - data4)

print('--------------------------------------')
data3.add(100)
print(data3)
data3.update([-3,-4,-5])
print(data3)
data3.remove(8)
print(data3)



